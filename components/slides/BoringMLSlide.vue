1. Definition von Maschinellem Lernen
   - Maschinelles Lernen ist ein Bereich der Künstlichen Intelligenz, der sich auf die Entwicklung von Algorithmen konzentriert, die es Computern ermöglichen, aus Daten zu lernen und Muster zu erkennen, um Entscheidungen zu treffen oder Vorhersagen zu treffen.

2. Lernen wie beim Menschen
   - Maschinelles Lernen ahmt den menschlichen Lernprozess nach, indem es aus Erfahrungen lernt und darauf aufbaut, um zukünftige Entscheidungen oder Aktionen zu optimieren.

3. Lernen ohne explizite Anweisungen
   - Im Gegensatz zur traditionellen Programmierung, bei der man einem Computer explizite Anweisungen gibt, ermöglicht maschinelles Lernen, dass Computer selbstständig lernen und sich an neue Situationen anpassen können.

4. Beispiele für maschinelles Lernen
   - Zum Beispiel kann die Codevervollständigung in IntelliJ auf maschinelles Lernen basieren und dabei helfen, Code-Snippets basierend auf den bisher eingegebenen Mustern vorzuschlagen.

5. Paradigmenwechsel in der Softwareentwicklung
   - Maschinelles Lernen eröffnet neue Möglichkeiten in der Softwareentwicklung, indem es auf das Lernen aus Daten und Mustern setzt, anstatt auf explizite Programmierung.

6. Optimierung von Modellparametern
   - Im maschinellen Lernen werden die Modellparameter durch Verfahren wie den Gradientenabstieg optimiert, um die Genauigkeit der Vorhersagen oder Entscheidungen zu verbessern.

7. Verschiedene Lernziele
   - Es gibt verschiedene Arten von maschinellem Lernen, wie zum Beispiel:
      a) Überwachtes Lernen: Hierbei lernt das Modell aus Daten, die mit einer Zielvariablen (Labels) versehen sind, um zukünftige Vorhersagen zu treffen.
      b) Unüberwachtes Lernen: Ziel ist es, verborgene Strukturen wie Cluster, Zusammenhänge oder Anomalien in den Daten ohne Labels zu erkennen.
      c) Verstärkungslernen: Ein Modell lernt, optimale Entscheidungen zu treffen, indem es mit seiner Umgebung interagiert und Belohnungen/Strafen für seine Aktionen erhält.

